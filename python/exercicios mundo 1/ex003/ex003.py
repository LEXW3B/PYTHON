n = float(input('digite um numero: '))
print('o dobro do numero digitado é {}'.format(n * 2))
print('o triplo do numero digitado é {}'.format(n * 3))
print('a raiz do numero digitado é {:.2f}'.format(n ** (1/2)))#pra fazer a raiz quadrada o meio tem que estar entre parenteses. Esse///// :.2f //////é para aparecer so dois numeros depois da virgula.

